// Classe Book
public class Pizza {
    private String title;
    private String tamanho;

    public Pizza(String title, String tamanho) {
        this.title = title;
        this.tamanho = tamanho;
    }

    public String getTitle() {
        return title;
    }

    public String getTamanho() {
        return tamanho;
    }
}
